let studentName = "Navi";  // String value
let studentAge = 27;           // Numeric value
let isEnrolled = true;         // Boolean value

// String: A text value
console.log("String:", studentName);    //prints value of studentName variable

// Number: An integer or floating-point number
console.log("Number:", studentAge);     //prints value of studentAge variable

// Boolean: true or false
let isGraduated = false;
console.log("Boolean:", isGraduated);       //prints value of isGraduated boolean variable

// Array: A collection of values
let favoriteFruits = ["apple", "banana", "cherry"];
console.log("Array:", favoriteFruits);      //prints array

// Object: An entity with properties
let student = {
    firstName: "Navi",
    lastName: "Ranu",
    age: 27,
    isEnrolled: true
};
console.log("Object:", student);    //prints object

// Arithmetic Operators
let a = 10;
let b = 5;

console.log("Addition (a + b):", a + b);    
console.log("Subtraction (a - b):", a - b);        
console.log("Multiplication (a * b):", a * b);      
console.log("Division (a / b):", a / b);            

// Comparison Operators
let x = 8;
let y = 12;

console.log("Greater than (x > y):", x > y);        
console.log("Less than (x < y):", x < y);           
console.log("Equal to (x === y):", x === y);        
console.log("Not equal (x !== y):", x !== y);       

// Logical Operators
let isSunny = true;
let hasUmbrella = false;

console.log("AND (isSunny && hasUmbrella):", isSunny && hasUmbrella);  
console.log("OR (isSunny || hasUmbrella):", isSunny || hasUmbrella);  
console.log("NOT (!isSunny):", !isSunny);                              
